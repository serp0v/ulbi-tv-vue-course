<template>
    <form @submit.prevent>
        <h4>Создание поста</h4>
        <my-input style="margin-bottom: 5px;" v-model="post.title" type="text" placeholder="Название" />

        <my-input v-model="post.body" placeholder="Описание" />

        <my-button
        @click="createPost"
        class="btn" style="align-self: flex-end; margin: 15px 0 15px 0;">Создать</my-button>
    </form>
</template>

<script>
export default {
    data() {
        return {
            post: {
                title: '',
                body: ''
            }
        }
    },
    methods: {
        createPost() {
            this.post.id = Date.now();
            this.$emit('create', this.post);
            this.post = {
                title: '',
                body: ''
            }
        },
    }
}
</script>

